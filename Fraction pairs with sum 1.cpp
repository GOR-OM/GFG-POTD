class Solution
{
    public:
    int countFractions(int n, int num[], int deno[])
    {
        int cnt=0;
        map<double,int> mp;
        for(int i=0; i<n; i++){
            double t1= (double)(num[i])/deno[i];
            double t2= (double)(deno[i]-num[i])/deno[i];
            cnt+= mp[t2];
            mp[t1]++;
        }
        return cnt;
    }
};

